def parsing_text( _txt:str ):
	slash_cnt = _txt.count( '/' )
	if( slash_cnt != 2 ):
		return False, " '/'의 개수가 2개가 아닙니다."

	try:
		items = _txt.split( '/' )
		name = items[ 0 ].strip()
		for key in rank_list.keys():
			if name.find( key ) >= 0:
				name = key
				break

		total_time = items[ 1 ]
		remark = items[ 2 ].strip()

		times = total_time.split( '~' )
		start_time = times[ 0 ].strip()
		if start_time == None or start_time == '':
			return False, "시작 시간 오류"

		end_time = times[ 1 ].strip()
		if end_time == None or end_time == '':
			return False, "종료 시간 오류"

		rank = rank_list.get( name )
		if rank == None:
			rank = "연구원"

		today_file_name = get_toady_file_name()
		print( 'today_file_name : ' + today_file_name )
		file_exist = os.path.isfile( today_file_name )
		print( 'file_exist : ' + str(file_exist) )
		if file_exist :
			wb = openpyxl.load_workbook( today_file_name )
		else :
			wb = openpyxl.load_workbook( template_file_name )

		sheet = wb[ '서식' ]

		for row in range( 4, 22 ) :
			sh_name = sheet.cell( row=row, column=1 ).value
			if sh_name == '' or sh_name == None or sh_name == name :
				print( f'row:{row}, {name}, {rank}, {start_time}, {end_time}, {remark}' )
				sheet.cell( row=row, column=1 ).value = name
				sheet.cell( row=row, column=2 ).value = rank
				sheet.cell( row=row, column=3 ).value = datetime.now( timezone( 'Asia/Seoul' ) ).strftime( '%Y/%m/%d' )
				sheet.cell( row=row, column=4 ).value = start_time
				sheet.cell( row=row, column=5 ).value = end_time
				sheet.cell( row=row, column=6 ).value = '안성사업장'
				sheet.cell( row=row, column=7 ).value = remark
				sheet.cell( row=row, column=9 ).value = '평일'
				sheet.cell( row=row, column=10 ).value = '2:00'
				break
		
		
		wb.save( filename=today_file_name)
		print( f'wb.save({today_file_name}) complete' )

		file_exist = os.path.isfile( today_file_name )
		if( file_exist != True ):
			print( f'{today_file_name} final check fail, not exist' )
			return False, f"{today_file_name} 파일이 존재하지 않습니다. 다시 신청해야 합니다."
		else:
			return True, ""

	except Exception as error:
		print( repr(error) )
		return False, repr(error)